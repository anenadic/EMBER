<p>At the end of the practical are <strong><font color=brown>Final assessment</font></strong>
  and <strong><font color=brown>Appendix</font></strong> sections. The former contains a <strong>Final quiz</strong>, 
  to help you assess what you have learned; it also contains a <strong>Feedback</strong> 
  section with a questionnaire and a contact us form, to help us make improvements. In 
  the <strong><font color=brown>Appendix</font></strong> is a <strong>Glossary</font></strong> of bioinformatics-related terms and a <strong>Contributors</strong> page.</p>

 <p class="sectionheader"><br>Using the practical</p>
<div align="left"> 
Always
 <ul>
  <li>Keep in mind a Chapter's <font color=blue>aims</font>.</li>
  <li>In the <font color=green>instructions</font>, 
<ul>
  <li>Read <em>NOTE</em>s in each Step carefully. </li>
  <li>Keep accession numbers, sequences and any other important results using the Notepad provided (remember to save your results - the Notepad will not keep data between sessions).</font></li>
  <li>If the services are slow, do make use of the alternative links provided.</font></li> 
  <li>In the <em>Reflections...</em> 
  
   <ul>
    <li>take time to answer all the questions. </font></li>
    <li>Follow the <em><u>HINT</u></em>s to help find an answer. </font></li>
    <li>As before, note your answers.</font></li>
   </ul>
  </li>
  </ul>
  <li>In the <font color=brown>info</font>, 
   follow links to online documentation - they help you understand important principles and to answer questions. </font></li>
  <li>Repeat the <font color=purple>quiz</font> until you get the questions right.</font></li>
 </ul>
