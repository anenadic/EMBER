           <p>The purpose of this practical is to introduce a range of bioinformatics services, databases 
  and software available on the Web and to illustrate their advantages and disadvantages. 
  The practical consists of 
  three tutorials, each comprising several Chapters.</p>
           <p align="left">The <strong> <font color=brown>
 Basic tutorial</font></strong> deals with DNA translation, similarity searching of protein sequence and 
 family databases, sequence alignment, protein structure classification and tools for secondary structure 
 prediction. The <strong><font color=brown>Advanced tutorial</font></strong> 
builds on these approaches, introducing more advanced techniques of homology modelling and fold recognition. 
Finally, the 
 <strong><font color=brown>Case studies</font></strong> provide an opportunity to apply 
 all of these techniques - and new ones - to credible biological problems.</p>

