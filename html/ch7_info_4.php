<p class="sectionheader"><a name="rmodels"></a>7.4 Application of homology
    models</p>
<p>For a homology model to be accurate, 
 the templates from which it is built must possess significant sequence similarity. 
 Though a model may look convincing in a PDB viewer, it is important to remember 
 that the structure obtained is just that - a model. Even with these reservations, 
 at a time where there are many more sequences than structures, homology modelling 
 is a useful tool. Models may be used to guide the drug discovery process, in 
 the absence of other structural data, especially if used in conjunction with 
 other experimental data. </p>
<p>In situations where a model is required 
 but there is insufficient sequence similarity to a known 3D-structure, other 
 methods may be employed. Examples of these are the subject of <a href="ch8_aims.php">Chapter
 8</a>.</p>



