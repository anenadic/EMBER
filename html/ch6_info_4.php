          <a name="_Ref82778"></a>6.4 SCOP </p>
          <p>The
  <?php $Url_id="106"; $Link_naam="SCOP"; include ('get_link.php'); ?> (Structural Classification Of Proteins) database describes
 <?php $Url_id="107"; $Link_naam="structural and evolutionary relationships"; include ('get_link.php'); ?> between proteins of known structure. The database has been constructed using
 a combination of manual inspection and automated methods, because current automatic
 sequence and structure comparison tools cannot identify all structural relationships
 reliably. Proteins are classified in a hierarchical fashion to reflect their
 structural and evolutionary relatedness. </p>
<p>Within the hierarchy, the principal levels describe the family, superfamily
 and fold: proteins are clustered into families with clear evolutionary relationships
 if they have sequence identities &gt;= 30% (but this is not an absolute measure);
 proteins are placed in superfamilies when, in spite of low sequence identity,
 their structural and functional characteristics suggest a common evolutionary
 origin; and proteins are classed as having a common fold if they have the same
 major secondary structures in the same arrangement and with the same topology,
 whether or not they have a common evolutionary origin. In these cases, the
 structural similarities could have arisen as a result of physical principles
 that favour particular packing arrangements and fold topologies.</p>
<p>Boundaries between such levels may be subjective, but the higher levels generally
 reflect the clearest structural similarities. 
