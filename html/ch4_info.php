<p class="sectionheader"><a name="intro"></a>4.1 Multiple Sequence
  Alignment</p>
<p>Multiple 
 sequence alignments are a tabular description of the relationships between
 proteins, where rows represent individual sequences, and the columns the residue
 positions. 
 Similar residues are brought into vertical register by introducing gaps, so
  that the relative position of residues within the alignment is preserved.
 The 
 result is an expression of the similarities and dissimilarities between the
  sequences. </p>
<p>Sequence 
 alignments have proven to be useful in many aspects of sequence analysis: <em>e.g</em>., 
 they form the basis of similarity searches (<a href="ch2_aims.php">Chapter
 2</a>) and the substitution 
 matrices that underlie these techniques; and they provide seed data for the 
 methods used in protein family databases (<a href="ch3_aims.php">Chapter 3</a>). </p>
<p>A multiple 
 sequence alignment is a model that allows sequences to be compared and their
  evolutionary relationships assessed. The more divergent the sequences, the
 more 
 distant the structural and functional relationships, and the more difficult
  it is to perform the alignment. Fig 4.1 illustrates how higher sequence identity
 
 lends more confidence to the inference of homology. </p>
<p>As alignments 
 underpin so many aspects of sequence analysis, many tools and repositories have 
 been developed to help build and store them.</p>
